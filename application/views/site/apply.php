        <h2>The 2-hour lab</h2> <p>should be an opportunity for students to work on the project with other team members. Students are not expected to finish everything during the lab; instead, students will use the time to meet with his/her team members and work on the project together. Students are encouraged to meet online or offline with the team members to discuss and work on the project throughout the week. There will be weekly assignments that will require the students to implement some features of the Ego project, except the first two weeks, during which students will practice working with GitHub and CodeIgniter MVC Framework. In some weeks, there will be readings to prepare students for the following week's discussion. (ex. If we are going to implement RSS feed system by keywords, we might give a brief reading about how RSS works, as well as some intro to XML. </p>
