    <h1>The Objectives | </h1><br /><br />
    <h2>Students will...</h2><br />
    <div class="list">
    <h2>1. apply </h2> <p>what they have learned in class to a real-world application. Education is more than learning dry theory. Rather, it involves real-world applications, namely group projects like Ego. </p><br /><br />
    <h2>2. learn </h2> <p>how to work in a team. Coding alone and coding in a team is a vastly different experience. Coding alone is like writing an essay for only you to see it. In a group, you need to properly encapsulate your modules, adhere to the framework, etc. As a result, you will learn to become a better programmer, one who is capable of working efficiently in a team - a quality that may be more important than writing a more efficient code.</p><br /><br />
    <h2>3. introduced </h2> <p>to many of the design techniques that are used in software engineering in a large team. Here are the highlights of the techniques students will learn: <h2>MVC (Model-View-Controller) Framework</h2> - Students will be using CodeIgniter PHP framework, which uses MVC architectural pattern in software engineering. In MVC framework, there will be a strict division of labor/concern in application development, namely Model(business logic), View(User Interface), and Controller (User Input). This allows the team to develop independently, test quickly, and manage more efficiently. <h2>Revision Control (Source Control)</h2> - Students will be using GitHub to learn how to work with Git revision control/source code management system. GitHub allows teams to manage large amount of codes efficiently by making sure changes are orderly, keeping track of revision history, creating a set of rules for the team to follow, and managing a backup in case of emergency. </p><br /><br />
    <h2>4. combine</h2> <p>various techniques, including PHP, HTML 5, CSS, jQuery, and XML to create a revolutionary feed reader. We believe this will be a valuable experience for students by participating in an effort to create something new that will help the users, rather than staying as a "user" who uses existing products. </p>
